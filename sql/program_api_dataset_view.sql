create or replace view program_api_dataset as
select
    pdc.document_id,
    pdc.match_iteration_id,
    pdc.program_description_id,
    pm.program_stream_id,
    d.discipline_id,
    d.discipline as discipline,
    pm.discipline_name,
    pm.school_id,
    pm.school_name,
    pm.program_site,
    pm.program_stream,
    pm.program_stream_name,
    pm.program_name as master_program_name,
    pdc.program_name as raw_program_name,
    coalesce(pm.program_url, pdc.source) as source_url,
    pdc.source as source_document_url,
    pdc.content_language,
    pdc.n_program_description_sections,
    pdc.non_empty_section_count,
    pdc.section_text_total_chars,
    pdc.has_program_contacts,
    pdc.has_general_instructions,
    pdc.has_supporting_documentation_information,
    pdc.has_review_process,
    pdc.has_interviews,
    pdc.has_selection_criteria,
    pdc.has_program_highlights,
    pdc.has_program_curriculum,
    pdc.has_training_sites,
    pdc.has_additional_information,
    pdc.has_return_of_service,
    pdc.has_faq,
    pdc.has_summary_of_changes,
    pdc.program_contacts,
    pdc.general_instructions,
    pdc.supporting_documentation_information,
    pdc.review_process,
    pdc.interviews,
    pdc.selection_criteria,
    pdc.program_highlights,
    pdc.program_curriculum,
    pdc.training_sites,
    pdc.additional_information,
    pdc.return_of_service,
    pdc.faq,
    pdc.summary_of_changes,
    pdc.created_at,
    pdc.updated_at
from program_description_content pdc
join program_master pm
    on pm.program_stream_id = pdc.program_description_id
join discipline d
    on d.discipline_id = pm.discipline_id;
